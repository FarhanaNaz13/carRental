    private void txtcaridKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcaridKeyPressed
        // TODO add your handling code here:

        if(evt.getKeyCode() == KeyEvent.VK_ENTER) {

            try {
                Class.forName("com.mysql.jdbc.Driver");
                com.mysql.jdbc.Connection conn = (com.mysql.jdbc.Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/carrental", "root", "");
                PreparedStatement pst = (PreparedStatement) conn.prepareStatement("select  cust_id,due,DATEDIFF(NOW(),due) as elap from rental where car_id=?");

                String car_id = txtcarid.getText();
                pst.setString(1,car_id);
                ResultSet rs = pst.executeQuery();
                if(rs.next()== false)
                {
                    JOptionPane.showMessageDialog(this,"Car ID not availabe");

                }else{
                    String custid = rs.getString("cust_id");
                    txtcustid1.setText(custid.trim());
                    String date= rs.getString("due");
                    txtdate.setText(date.trim());

                    String elap = rs.getString("elap");
                    int elaped = Integer.parseInt(elap);
                    if(elaped>0)
                    {
                        txtelap.setText(elap);
                        int fine = elaped * 100;
                        txtfine.setText(String.valueOf(fine));
                    }
                    else{
                        txtelap.setText("0");
                        txtfine.setText("0");
                    }
                }

            }catch(ClassNotFoundException ex) {
                Logger.getLogger(rental.class.getName()).log(Level.SEVERE, null, ex);
            }catch (SQLException ex) {
                Logger.getLogger(carRegistration.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_txtcaridKeyPressed

