    private void button2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button2ActionPerformed
        // TODO add your handling code here:
        String car_id = txtcarid.getText();
        String Cust_id = txtcustid1.getText();
        String date = txtdate.getText();
        String elp = txtelap.getText();
        String fine = txtfine.getText();
        try {
            Class.forName("com.mysql.jdbc.Driver");

            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/carrental", "root", "");
            PreparedStatement pst = (PreparedStatement) conn.prepareStatement("insert into returncar(carid,custid,return_date,elap,fine)values(?,?,?,?,?)");
            pst.setString(1, car_id);
            pst.setString(2, Cust_id);
            pst.setString(3, date);
            pst.setString(4, elp);
            pst.setString(5, fine);
            pst.executeUpdate();

            java.sql.PreparedStatement pst1 = conn.prepareStatement("update carregistration set available ='Yes' where car_no=?");
            pst1.setString(1, car_id);
            pst1.executeUpdate();

            java.sql.PreparedStatement pst2 = conn.prepareStatement("Delete from rental where car_id=?");
            pst2.setString(1,car_id);
            pst2.executeUpdate();

            JOptionPane.showMessageDialog(this,"Car updated");
        }catch (ClassNotFoundException ex) {
            Logger.getLogger(rental.class.getName()).log(Level.SEVERE, null, ex);
        }catch (SQLException ex) {
            Logger.getLogger(carRegistration.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_button2ActionPerformed

